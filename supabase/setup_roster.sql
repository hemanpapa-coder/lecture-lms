CREATE TABLE IF NOT EXISTS public.student_roster (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  course_id uuid REFERENCES public.courses(id) ON DELETE CASCADE,
  no integer,
  department text,        -- 학부(과)
  major_class text,       -- 학과반
  grade integer,          -- 학년
  student_number text,    -- 학번
  name text,              -- 성명
  phone text,             -- 연락처
  early_employment boolean DEFAULT false, -- 조기취업여부
  note text,              -- 비고 (악기/전공)
  created_at timestamptz DEFAULT now()
);

ALTER TABLE public.student_roster ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Admin can manage roster" ON public.student_roster;
CREATE POLICY "Admin can manage roster" ON public.student_roster FOR ALL USING (public.get_my_role() = 'admin');
DROP POLICY IF EXISTS "Students can view roster" ON public.student_roster;
CREATE POLICY "Students can view roster" ON public.student_roster FOR SELECT USING (true);

